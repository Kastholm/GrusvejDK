<h5>Hvad kan vi gøre for at sikre vores veje</h5>
<p>”Løsningen kaldes opklassificering”, fortæller Lars-Henrik Jensen fra Grusvej.dk. ”Der skal
gøres noget effektivt for at skabe den nødvendige energi og holdbarhed i vejene, så de
kan holde i mange år frem på trods af de stigende vandmængder”.
<br><br>
En af løsningerne er genopretning af de nuværende grus- og jordveje. Grusvej.dk har
udviklet flere processer som kan fremtidssikre vejene. Metoder som er både effektive og
klimavenlige.
De gamle veje knuses på stedet med tungt specialmaskineri for at findele fortidens
nedlagte materialer, hvorefter vejen stabiliseres med kalk eller cement og der udlægges et
mindre supplerende bærelag/slidlag på vejen.
