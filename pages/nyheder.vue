naturligt ledes væk fra overfladen – eventuelt ud i anlagte regnvandsbassiner til
opsamling. <br><br>
I og med at processen foregår med de eksisterende materialer ”on location” er der ikke
brug for at fragte råmaterialer til og fra. Det betyder sparede CO2 udledninger.
Der er igennem tiden etableret grøfter og kanaler for afledning af vand i terrænet på
mange steder, hvor vedligeholdelsen ikke har været nødvendig fordi grundvandet har stået
lavt. <br><br>
Med det forhøjede grundvandsspejl og med de dystre udsigter som varsles i fremtiden, er
der behov for at en del af de gamle afledningssystemer vækkes til live igen.
