ligesom genopretningen kan gøres helt komplet med støvbindingsmidlet Dustex. Midlet,
som er et restprodukt fra papirproduktion, fuldstændigt giftfrit og uden påvirkning af
naturen. Dustex går i forbindelse med jord- og støvpartikler, og binder materialerne til en
hård og ultraholdbar overflade.</p>
<h5>Manglen på råstoffer</h5>
<p>Danmarks råstoffer er blevet knappe, vi skal spare eller finde på nye løsninger.
Stabilisering er en af de nye metoder som vil blive anvendt i fremtiden, påpeger Lars-
Henrik Jensen. <br><br>
”Med tilførsel af kalk eller cement i råjorden øges bæreevnen ligesom tilførsel af cement
eller ligning i bundsikrings- og bærelaget reducerer forbruget af råstoffer og dermed
reducerer vores co2 forbrug væsentligt.”<br><br>
”Vi skal gøre os meget større tanker om at reducere vores Co2 og råstofforbrug når det
gælder vores mange grus- og jordveje. Vi står lige nu midt i udfordringen - heldigvis med
mulighed for at gøre noget effektivt ved det”, slutter Lars-Henrik Jensen fra Grusvej.dk</p>
